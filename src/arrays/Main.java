    private void jButton_doExercise29Part1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_doExercise29Part1ActionPerformed
        int[] A = new int[20]; // Создание нового массива A

        A = Generate.generateArray(A, 100, 0);

        // Поиск самого минимального значения в массиве
        // Создаем переменную, которая будет хранить самое минмальное значение в массиве A
        // Изначально присваиваем этой переменной самое максимальное значение
        int min = Integer.MAX_VALUE;
        int min_i = -1; // Эта переменная хранит индекс самого минимального числа в массиве
        for (int i = 0; i < A.length; i++) {
            if (i % 2 == 0) { // Определяем четность индекса
                // Math.min вернет самое маленькое число из min и A[i].
                // Таким образом у нас в мин всегда будет самое минимальное число
                if (A[i] < min) {
                    min = Math.min(min, A[i]);
                    min_i = i; // Записываем индекс нового самого минимального элемента
                }
            }
        }

        // Выводим результат
        jLabel_outputMinValueExercise29Part1.setText("Минимальное значение: " + Integer.toString(min));
        jLabel_outputIndexMinValueExercise29Part1.setText("Индекс минимального значения: " + Integer.toString(min_i));
        jLabel_outputArrayExercise29Part1.setText("Массив" + Arrays.toString(A));
    }//GEN-LAST:event_jButton_doExercise29Part1ActionPerformed

