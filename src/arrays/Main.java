    public static Path createFile(String path_of, String pathOfDirectory, String name) {
        Path path = Path.of(path_of); // Пытаеться получить путь до создаваемого файла для проверки его существования
        Path directory = Path.of(pathOfDirectory); // Хранит путь до директории
        if (!Files.exists(path)) { // Проверяем существует ли файл
            try {
                path = Files.createFile(directory.resolve(name)); // Создаем файл
            } catch (IOException ex) { // Отлавливает ошибки и если находит то записывает в логгер
                java.util.logging.Logger.getLogger(Main.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
        return path;
    }

    public static void writeFile(Path path, String to_write) {
        try {
            Files.writeString(path, to_write, StandardCharsets.UTF_8); // Записываем строку в файл
        } catch (IOException ex) { // Отлавливает ошибки и если находит то записывает в логгер
            java.util.logging.Logger.getLogger(Main.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    public static void createAndWriteFile(String path_of, String path_of_directory, String name, String to_write) {
        Path path = createFile(path_of, path_of_directory, name); // Переменная которая хранит путь до новосозданного файла
        if (path != null) { // Путь существует
            writeFile(path, to_write); // записываем в него информацию из to_write
        }
    }

    public static List<String> readFromFile(String path_of) {
        Path path = Paths.get(path_of); // Получаем путь до файла
        List<String> result = null; // Будет хранить результат

        try {
            result = Files.readAllLines(path); // Считываем все строки из файла
        } catch (IOException ex) { // Отлавливает ошибки и если находит то записывает в логгер
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

        return result;
    }

